# Arithmetic Operator
x = 10
y = 5
print("x + y is ", x + y)
print("x - y is ", x - y)
print("x * y is ", x * y)
print("x / y is ", x / y)

# Assignement operator

x1 = 12
x2 = 13
print(x1>x2)
print(x1<x2)
print(x1==x2)
print(x1!=x2)

# Logical Operator

s1 = 22
s2 = 33
print(s1 == s2 and s1<s2)
print(s1 == s2 or s1<s2)
print(not True)
print(not False)
